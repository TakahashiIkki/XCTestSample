class MathFunction {
    
    /// 値を足して和を返す関数
    ///
    /// - Parameters:
    ///   - addedValue: 被加数(足される数)
    ///   - addValue:   加数(足す数)
    /// - Returns: 被加数 と 加数 の和
    func add(addedValue: Int, addValue: Int) -> Int {
        return addedValue + addValue
    }
}
